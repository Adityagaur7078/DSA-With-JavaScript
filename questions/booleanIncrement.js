function runBooleanIncrement() {

    // Question
    let d = true;
    d++;
    console.log(d);

    // Answer

    // true is converted to number 1
    // d++ increases 1 to 2

    // Final value:
    // 2


    // Question 
    let e = false;
    e++;
    console.log(e);

    // Answer

    // false is converted to number 0
    // e++ increases 0 to 1

    // Final value:
    // 1

}